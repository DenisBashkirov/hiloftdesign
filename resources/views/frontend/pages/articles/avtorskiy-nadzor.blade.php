            </figure>

            <p>Если бы авторского надзора не было, клиент получил бы совсем не тот дизайн, который заказывал.</p>

            <p><b>3. Дизайнер быстро подкорректирует проект.</b> Во время демонтажа рабочие могут обнаружить какие-то скрытые моменты, которые приведут к изменениям в проекте. Для таких случаев нужен авторский надзор: автор, который знает все тонкости проекта, сможет оперативно скорректировать детали на месте.</p>

            <p>Авторский надзор — это полное сопровождение проекта, не обязательная, но важная услуга. Заказывать ее следует после того, как будет готов альбом рабочей документации. </p>

            <h2>Как мы подходим к организации авторского надзора</h2>

