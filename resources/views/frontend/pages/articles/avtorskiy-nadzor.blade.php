            <h2>Подведём итоги</h2>

            <p>Контроль за ходом ремонтных и строительных работ стоит поручить авторам дизайн-проекта. Они могут не только проследить, чтобы всё было реализовано в соответствии с рабочей документацией, но и внести коррективы по необходимости.</p>

            <p>Доверяя строителям и другим подрядчикам, вы рискуете получить дизайн, который не заказывали. Так может произойти по двум причинам:</p>

            <ul>
                <li>исполнители закупят не те материалы, которые были в спецификации, а те, которые смогут найти, что ухудшит интерьер;</li>
                <li>во время ремонта подрядчики могут неверно понять чертежи и эскизы, и, если спросить будет не у кого, они сделают так, как посчитают нужным.</li>
            </ul>

            <p>Дизайнеры же проследят за тем, чтобы исполнители закупили нужные материалы и мебель, смогут быстро подкорректировать чертежи и ответить на вопросы подрядчиков. Вы можете заниматься своими делами и не переживать о результате. </p>

