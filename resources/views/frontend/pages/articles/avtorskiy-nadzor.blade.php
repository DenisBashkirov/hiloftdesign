            <h2>Три причины заказать проведение авторского надзора</h2>

            <p><b>1. Дизайнеру важен результат</b>. Он разработал проект, поэтому будет следить за тем, чтобы подрядчики придерживались документации. В процессе ремонта добросовестный специалист будет высчитывать миллиметры, добиваясь точных размеров и искать нужный оттенок краски для стен.</p>

            <p><b>2. Автор сможет проконсультировать строителей.</b> Присутствуя на объекте, он подскажет подрядчикам, почему важно в точности соблюсти размеры, как должны располагаться светильники и в какой цвет следует выкрасить стены. Даже мелкие, на первый взгляд, нарушения могут ухудшить вид помещения.</p>

            <p>Вот еще один случай из практики. По задумке дизайнеров строителям нужно было выкрасить короб в санузле. Они сделали все верно — взяли артикул краски, прописанный в проекте. Двери в санузлах тоже нужно было покрасить, но краской потемнее. Вероятно, увидев, что двери получились темнее короба, решили покрасить его в тон дверей, хотя этого не нужно было делать. В итоге санузел получился не таким, как было задумано.</p>

