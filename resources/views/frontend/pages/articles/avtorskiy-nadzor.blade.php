                <li>Контроль и присутствие при важных монтажных работах. Следим за монтажом освещения, потолочных конструкций, напольных покрытий и заказных изделий.</li>
                <li>Надзор за авторской реализацией дизайн-проекта. Все ремонтные работы сверяем с чертежами и спецификациями, чтобы ничего не упустить, консультируем строителей по эскизам. При необходимости корректируем и дополняем рабочую документацию;</li>
                <li>Ведение журнала в электронном и бумажном виде. По нему легко отследить, когда и какие работы выполнялись, что готово, что пошло не по плану. В любой момент вы сможете проверить, почему сроки затянулись и на что потрачены деньги.</li>
