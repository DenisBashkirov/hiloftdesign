            <h2 id="header-1">Почему не стоит отказываться от проведения авторского надзора</h2>

            <p>Если вы решите вести проект самостоятельно, будьте готовы к тому, что придется одновременно контролировать два процесса: комплектацию и стройку. Всё это отнимает много сил и времени. Вам будут задавать вопросы менеджеры компаний-поставщиков мебели, оборудования, отделочных материалов, ремонтная бригада и другие подрядчики:</p>

            <ul class="list-circle">
                <li>как быть, если нужной краски не оказалось, и можно ли заменить ее похожей, но другой марки?</li>
                <li>что делать, если между мебелью и встроенной нишей появились щели?</li>
                <li>как подкорректировать проект, если во время ремонта потолок стал ниже, а стены получились более узкими, чем было задумано?</li>
            </ul>

            <p>И таких вопросов по мере того, как продвигается стройка, становится только больше. Вас постоянно будут дергать, просить разъяснить чертежи и спецификации, невзирая на то что вы не строитель и не дизайнер.</p>

            <p>Казалось бы, почему возникают вопросы с комплектацией и ремонтом, если на руках у строителей рабочая документация? Там подробно прописаны все материалы и мебель — просто бери и делай. Причин несколько.</p>

            <p><b>Строители или ваши менеджеры по закупкам не участвовали в разработке дизайн-проекта.</b> Они не понимают, почему дизайнеры подобрали именно такие мебель и материалы, — если чего-то не оказалось, берут то, что, по их мнению, подойдет. Их цель — выполнить задачу быстро и не потратить лишнего. О возможных последствиях самодеятельности мы писали в статье «Что такое комплектация дизайн-проекта и кому ее доверить, чтобы не разочароваться в результате».</p>

            <p>Стройка — это гибкий процесс. Чтобы воплотить дизайнерскую задумку, мало просто посмотреть на чертежи. Во время стройки могут вскрыться моменты, которые нереально было учесть при визуальном осмотре помещения. Поэтому иногда в моменте эскизы и чертежи приходится корректировать: что-то убирать, что-то добавлять. Важно делать это так, чтобы не испортить дизайн. Строители — не авторы проекта, поэтому они не знают, как сделать лучше. Раз спросить больше не у кого, идут к клиенту.</p>

