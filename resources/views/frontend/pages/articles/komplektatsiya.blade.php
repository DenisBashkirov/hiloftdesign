            <ul class="list-disc">
                <li><b>Плановое посещение объекта.</b> Встреча занимает до двух часов, дата и время согласовываются с клиентом;</li>
                <li><b>Подбор и контроль поставок мебели, сантехники, освещения, декоративных и отделочных материалов.</b> На этом этапе мы следим за тем, чтобы все позиции, указанные в рабочей документации, были доставлены в срок, соответствующего качества и в нужном количестве. Чтобы было проще контролировать процесс, оценивать расходы и сроки, наши менеджеры ведут сметы со сроками поставки и график строительных работ;</li>
