            <p>Чтобы подбирать для проекта современные материалы и мебель, мы отслеживаем мировые тренды дизайна интерьера общественных пространств, в частности, новинки скандинавского дизайна. Посещаем выставки в Милане и Кельне (раз в два года), Стокгольме (ежегодно), часто бываем на производствах в России и Европе, поставщики присылают информацию о новинках. Занимаясь комплектацией дизайн-проекта, подбираем не просто гармоничные, но и трендовые мебель и материалы.</p>

