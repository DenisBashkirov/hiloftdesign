            <p>На этапе комплектации специалисты дизайн-студии, разработавшей проект, закупают все необходимое для его воплощения: мебель, материалы и элементы декора. В Hiloft Design это не обязательная услуга, но 80% клиентов ее заказывают. Из оставшихся 20% у большинства есть собственные отделы закупок, и владельцы компаний уверены, что их сотрудники блестяще справятся с поставленной задачей. Реальность, увы, бывает не такой радужной — результат отличается от задумки, реализация затягивается, да и денег зачастую уходит больше, чем могло бы.</p>

