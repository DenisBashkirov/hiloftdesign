            <h2>Заключение</h2>

            <p>Если вы заказали разработку проекта в дизайн-студии, комплектацию объекта материалами также стоит доверить ее специалистам. Они знают концепцию проекта и смогут приобрести те материалы, которые указаны в рабочей документации, а также подобрать достойную альтернативу, если чего-то не окажется у поставщиков.</p>

            <p>Если решите поручить комплектацию строительной бригаде или своему отделу закупок, есть риск получить не тот интерьер, который вы согласовали. Так может случиться по нескольким причинам:</p>

            <ul class="list-disc">
                <li>исполнители не смогут найти нужные материалы и выберут замену на свой вкус;</li>
                <li>будут ориентироваться не на качество, а на скорость доставки или цену,  и в итоге выберут то, что может «убить» весь интерьер;</li>
                <li>строители неверно трактуют документацию и нарушают идею.</li>
            </ul>

            <p>Чтобы дизайн интерьера идеально совпал с проектом, важно все отдавать в одни руки: каждый этап должны контролировать авторы задумки. Только дизайнеры знают, какие материалы отлично впишутся в ваш интерьер, а также как подкорректировать чертежи и не испортить финальный дизайн интерьера.</p>

