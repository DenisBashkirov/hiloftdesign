            <p><b>Комплектация дизайн-проекта</b> — этап, на котором мы от имени клиента заказываем, обеспечиваем доставку и контролируем монтаж отделочных материалов и предметов интерьера согласно проекту на основе спецификаций, в которых содержатся:</p>

            <ul class="list-disc">
                <li>наименование;</li>
                <li>бренд;</li>
                <li>цвет;</li>
                <li>размер;</li>
                <li>поставщик;</li>
                <li>количество;</li>
                <li>изображение.</li>
            </ul>

